    # but 470 data for the extended categories

    # Flag the fact that R1 per-category rates differ between 7-cat and 470
    r1_7cat_cats = beh_7cat_models.get("r1_distill", {}).get("categories", {})
    r1_470_cats = canonical["behavioral"].get("r1_distill", {}).get("per_category_470", {})
