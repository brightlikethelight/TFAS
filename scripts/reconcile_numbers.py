    if "qwen_think" in beh_7cat_models:
        qwen_t_7cat = beh_7cat_models["qwen_think"]["overall"]
        paper_qwen_t_rate = 7.0
        data_qwen_t_rate = round(qwen_t_7cat["lure_rate"] * 100, 1)
