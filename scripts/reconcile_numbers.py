#!/usr/bin/env python3
"""
Reconcile numbers between data files and paper tex files.

Reads all authoritative data sources (bootstrap CIs, behavioral JSONs,
probe results, transfer matrices) and:
  1. Produces results/canonical_numbers.json with THE single source of truth.
  2. Scans the paper tex file and reports every mismatch.

The key methodology distinction:
  - Original pipeline: 5-fold CV on 330 items (7 categories, 165 pairs).
    Peak layers: Llama L14 (AUC=0.999), R1-Distill L16 (AUC=0.985).
  - Bootstrap pipeline: 1000-resample bootstrap on 160 vulnerable items
    (80 conflict / 80 no-conflict from 3 vulnerable categories).
    Peak layers: Llama L16 (AUC=0.974), R1-Distill L31 (AUC=0.930).
  - Cross-prediction was done at L14 (original pipeline peak).

For the paper: bootstrap CIs are the correct numbers for the main claims
because they come with proper uncertainty quantification. The 5-fold CV
numbers appear in the supplementary layer-by-layer tables.

Usage:
    python scripts/reconcile_numbers.py
"""
