# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
def load_json(path: Path) -> dict:
    with open(path) as f:
        return json.load(f)


def find_peak(layers: list[dict], key: str = "auc") -> dict:
    """Return the layer dict with the highest value for `key`."""
    return max(layers, key=lambda x: x[key])


def round3(v: float) -> float:
    return round(v, 3)


def round4(v: float) -> float:
    return round(v, 4)


