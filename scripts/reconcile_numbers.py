# 1. Build canonical numbers from authoritative data sources
# ---------------------------------------------------------------------------
def build_canonical() -> dict[str, Any]:
    """Build the single authoritative set of numbers."""
    canonical: dict[str, Any] = {
        "_metadata": {
            "description": (
                "Canonical numbers for the s1s2 paper. "
                "Generated by scripts/reconcile_numbers.py. "
                "All claims in the paper and supplementary MUST match these values."
            ),
            "methodology_note": (
                "Bootstrap numbers (peak_bootstrap) come from 1000-resample bootstrap "
                "on 160 vulnerable items (80 conflict / 80 no-conflict). "
                "CV numbers (peak_cv) come from 5-fold stratified CV on 330 items "
                "(7 categories, 165 pairs). Both are real; the paper uses bootstrap "
                "for main claims (with CIs) and CV for supplementary layer tables. "
                "Cross-prediction was done at L14 (CV peak)."
            ),
        },
        "behavioral": {},
        "probes_bootstrap": {},
        "probes_cv": {},
        "cross_prediction": {},
        "transfer_matrix": {},
        "cross_model_transfer": {},
        "lure_susceptibility": {},
    }

    # ---- Behavioral data ----
    # IMPORTANT: The paper uses DIFFERENT data sources for different numbers:
    #   - Overall rates (27.3%, 2.4%): from _ALL raw files (7 categories, 165 pairs)
    #   - Per-category rates (original 7): from _ALL raw files
    #   - Per-category rates (4 new cats): from 470-item data
    #   - Qwen overall (21%, 7%): from behavioral_complete (7 categories, 165 pairs)
    #   - OLMo: from 470-item data (only dataset for OLMo)
    # behavioral_complete.json has a MIX: Llama/R1 have 8 cats (190 pairs),
    #   Qwen has 7 cats (165 pairs), OLMo has 11 cats (235 pairs).
    #   The paper's overall rates do NOT match behavioral_complete for Llama/R1.

    beh_complete = load_json(BEHAVIORAL_7CAT)
    beh_470 = load_json(BEHAVIORAL_470)

    # Raw _ALL files: the TRUE source for the paper's headline Llama/R1 rates
    for mk, path, display in [
        ("llama", RESULTS / "behavioral" / "llama31_8b_ALL.json", "Llama-3.1-8B"),
        ("r1_distill", RESULTS / "behavioral" / "r1_distill_llama_ALL.json", "R1-Distill-8B"),
    ]:
        raw = load_json(path)
        results = raw["results"]
        n_conflict = sum(1 for r in results if r["conflict"])
        n_lure = sum(1 for r in results if r["conflict"] and r["verdict"] == "lure")
        canonical["behavioral"][mk] = {
            "display_name": display,
            "overall_7cat_raw": {
                "n_conflict": n_conflict,
                "n_lure": n_lure,
                "lure_rate": round4(n_lure / n_conflict),
                "lure_rate_pct": round(n_lure / n_conflict * 100, 1),
                "source": f"_ALL raw file ({n_conflict} conflict items, 7 categories)",
            },
        }

    # Qwen overall rates from behavioral_complete (7-cat subset, 165 pairs)
    for mk in ["qwen_nothink", "qwen_think"]:
        if mk in beh_complete["models"]:
            info = beh_complete["models"][mk]
            canonical["behavioral"][mk] = {
                "display_name": info["display_name"],
                "overall_7cat_raw": {
                    "n_conflict": info["overall"]["n_conflict"],
                    "n_lure": info["overall"]["n_lure"],
                    "lure_rate": round4(info["overall"]["lure_rate"]),
                    "lure_rate_pct": round(info["overall"]["lure_rate"] * 100, 1),
                    "source": "behavioral_complete (7 categories)",
                },
            }

    # Also store behavioral_complete overall for reference (may differ from raw)
    for mk, info in beh_complete["models"].items():
        if mk in canonical["behavioral"]:
            canonical["behavioral"][mk]["overall_beh_complete"] = {
                "n_conflict": info["overall"]["n_conflict"],
                "n_lure": info["overall"]["n_lure"],
                "lure_rate": round4(info["overall"]["lure_rate"]),
                "lure_rate_pct": round(info["overall"]["lure_rate"] * 100, 1),
            }

    # 470-item per-category rates (Table 1 in paper)
    for mk, info in beh_470["models"].items():
        if mk not in canonical["behavioral"]:
            canonical["behavioral"][mk] = {"display_name": info["display_name"]}
        canonical["behavioral"][mk]["overall_470"] = {
            "n_conflict": info["overall"]["n_conflict"],
            "n_lure": info["overall"]["n_lure"],
            "lure_rate": round4(info["overall"]["lure_rate"]),
            "lure_rate_pct": round(info["overall"]["lure_rate"] * 100, 1),
        }
        cats = {}
        for cat, cinfo in info["categories"].items():
            if cinfo is not None:
                cats[cat] = {
                    "n_conflict": cinfo["n_conflict"],
                    "n_lure": cinfo["n_lure"],
                    "lure_rate_pct": round(cinfo["lure_rate"] * 100, 0),
                }
        canonical["behavioral"][mk]["per_category_470"] = cats

    # OLMo behavioral from 470-item raw files (NOT from behavioral_complete which
    # may not have them)
    for olmo_key, olmo_path in [
        ("olmo_instruct", RESULTS / "behavioral" / "olmo3_instruct_ALL.json"),
        ("olmo_think", RESULTS / "behavioral" / "olmo3_think_ALL.json"),
    ]:
        raw = load_json(olmo_path)
        results = raw.get("results", [])
        overall_conflict = 0
        overall_lured = 0
        cats: dict[str, dict] = defaultdict(lambda: {"conflict": 0, "lured": 0})
        for item in results:
            if item.get("conflict", False):
                cat = item["category"]
                cats[cat]["conflict"] += 1
                overall_conflict += 1
                if item.get("verdict") == "lure":
                    cats[cat]["lured"] += 1
                    overall_lured += 1

        disp = "OLMo-3-7B-Instruct" if "instruct" in olmo_key else "OLMo-3-7B-Think"
        canonical["behavioral"][olmo_key] = {
            "display_name": disp,
            "overall_470": {
                "n_conflict": overall_conflict,
                "n_lure": overall_lured,
                "lure_rate": round4(overall_lured / overall_conflict) if overall_conflict else 0,
                "lure_rate_pct": round(overall_lured / overall_conflict * 100, 1) if overall_conflict else 0,
            },
            "per_category_470": {
                cat: {
                    "n_conflict": v["conflict"],
                    "n_lure": v["lured"],
                    "lure_rate_pct": round(v["lured"] / v["conflict"] * 100, 0) if v["conflict"] else 0,
                }
                for cat, v in sorted(cats.items())
            },
        }

    # ---- Bootstrap probe CIs (main paper claims) ----
    for model_key, path in BOOTSTRAP_FILES.items():
        data = load_json(path)
        model_info = {"model_hf": data["model"], "n_bootstrap": data["n_bootstrap"]}
        for cond_name, layers in data["conditions"].items():
            peak = find_peak(layers)
            ctrl = peak.get("control_auc")
            sel = peak.get("selectivity")
            model_info[f"peak_{cond_name}"] = {
                "layer": peak["layer"],
                "auc": round4(peak["auc"]),
                "ci_lower": round4(peak["ci_lower"]),
                "ci_upper": round4(peak["ci_upper"]),
            }
            if ctrl is not None:
                model_info[f"peak_{cond_name}"]["control_auc"] = round4(ctrl)
                model_info[f"peak_{cond_name}"]["selectivity"] = round4(sel)

            # Also store max control AUC across all layers
            if any("control_auc" in l for l in layers):
                max_ctrl_layer = max(
                    [l for l in layers if "control_auc" in l],
                    key=lambda x: x["control_auc"],
                )
                model_info[f"max_control_{cond_name}"] = {
                    "layer": max_ctrl_layer["layer"],
                    "control_auc": round4(max_ctrl_layer["control_auc"]),
                }

        canonical["probes_bootstrap"][model_key] = model_info

    # ---- 5-fold CV probes (supplementary layer tables) ----
    for model_key, path in CV_PROBE_FILES.items():
        data = load_json(path)
        canonical["probes_cv"][model_key] = {
            "best_layer": data["best_layer"],
            "best_auc": round4(data["best_auc"]),
            "categories": data.get("categories", "ALL"),
            "n_items": data.get("n_items"),
        }

    # ---- Cross-prediction (done at L14, original pipeline) ----
    for model_key, path in CROSS_PRED_FILES.items():
        data = load_json(path)
        layer_results = {}
        for layer_str, vals in data["results"].items():
            layer_results[int(layer_str)] = {
                "within_vulnerable": round4(vals["within_vulnerable"]),
                "transfer_to_immune": round4(vals["transfer_to_immune"]),
            }
        canonical["cross_prediction"][model_key] = {
            "model_name": data["model"],
            "layers": layer_results,
        }

    # ---- Transfer matrix (Llama, L14) ----
    tm = load_json(TRANSFER_MATRIX_FILE)
    canonical["transfer_matrix"] = {
        "model": "Llama",
        "layer": 14,
        "note": "Rows = train category, Cols = test category",
        "matrix": tm,
    }

    # ---- Cross-model transfer ----
    cm = load_json(CROSS_MODEL_FILE)
    for direction in ["llama_to_r1", "r1_to_llama"]:
        results = cm["results"][direction]
        peak = max(results, key=lambda x: x["auc"])
        canonical["cross_model_transfer"][direction] = {
            "peak_layer": peak["layer"],
            "peak_auc": round4(peak["auc"]),
        }

    # ---- Lure susceptibility ----
    for model_key, path in LURE_SUSCEPT_FILES.items():
        if path.exists():
            data = load_json(path)
            # Extract mean score
            if "mean_score" in data:
                canonical["lure_susceptibility"][model_key] = {
                    "mean": data["mean_score"],
                }
            elif "summary" in data:
                canonical["lure_susceptibility"][model_key] = data["summary"]
            else:
                canonical["lure_susceptibility"][model_key] = {
                    "keys": list(data.keys())[:10]
                }

    return canonical


# ---------------------------------------------------------------------------
# 2. Check paper tex against canonical numbers
# ---------------------------------------------------------------------------
class Mismatch:
    def __init__(
        self,
        inconsistency_id: int,
        severity: str,
        location: str,
        line: int | None,
        claim: str,
        canonical: str,
        fix: str,
    ):
        self.inconsistency_id = inconsistency_id
        self.severity = severity
        self.location = location
        self.line = line
        self.claim = claim
        self.canonical = canonical
        self.fix = fix

    def __str__(self) -> str:
        loc = f" (line {self.line})" if self.line else ""
        return (
            f"[#{self.inconsistency_id}] {self.severity} | {self.location}{loc}\n"
            f"  Paper says: {self.claim}\n"
            f"  Data says:  {self.canonical}\n"
            f"  Fix: {self.fix}"
        )


def check_paper(canonical: dict) -> list[Mismatch]:
    """Check the paper tex against canonical numbers."""
    mismatches: list[Mismatch] = []

    paper_text = PAPER_TEX.read_text()
    paper_lines = paper_text.split("\n")
    supp_text = SUPP_TEX.read_text() if SUPP_TEX.exists() else ""
    supp_lines = supp_text.split("\n") if supp_text else []

    def find_line(text: str, lines: list[str]) -> int | None:
        """Find the 1-indexed line number containing text."""
        for i, line in enumerate(lines):
            if text in line:
                return i + 1
        return None

    # ---- Bootstrap probe numbers ----
    llama_boot = canonical["probes_bootstrap"]["llama"]
    r1_boot = canonical["probes_bootstrap"]["r1_distill"]
    olmo_inst_boot = canonical["probes_bootstrap"]["olmo_instruct"]
    olmo_think_boot = canonical["probes_bootstrap"]["olmo_think"]

    llama_vuln = llama_boot["peak_vulnerable"]
    r1_vuln = r1_boot["peak_vulnerable"]

    # #1: Llama peak AUC and layer
    # Paper claims: 0.974 at L16 [0.952, 0.992]
    # Data: 0.9742 at L16 [0.9515, 0.9917] (bootstrap on vulnerable)
    # These are consistent (paper rounds to 3dp)
    paper_llama_auc = 0.974
    paper_llama_layer = 16
    data_llama_auc = llama_vuln["auc"]
    data_llama_layer = llama_vuln["layer"]

    if paper_llama_layer != data_llama_layer:
        mismatches.append(Mismatch(
            1, "CRITICAL", "Main paper (multiple locations)",
            find_line("0.974", paper_lines),
            f"Llama peak at L{paper_llama_layer}",
            f"Bootstrap peak at L{data_llama_layer}",
            f"Update peak layer to L{data_llama_layer}",
        ))

    if abs(paper_llama_auc - data_llama_auc) > 0.002:
        mismatches.append(Mismatch(
            1, "CRITICAL", "Main paper (multiple locations)",
            find_line("0.974", paper_lines),
            f"Llama peak AUC = {paper_llama_auc}",
            f"Bootstrap peak AUC = {data_llama_auc:.4f} (rounds to {round3(data_llama_auc)})",
            f"Update AUC to {round3(data_llama_auc)}",
        ))

    # Supplementary says L14 peak with AUC=0.999 -- that's from the CV pipeline
    # Both are correct but from different methodologies
    cv_llama = canonical["probes_cv"]["llama_all"]
    mismatches.append(Mismatch(
        1, "MODERATE", "Supplementary Table C.1 vs. main paper",
        None,
        (f"Supplementary bolded L{cv_llama['best_layer']} as peak "
         f"(AUC={cv_llama['best_auc']:.4f}, 5-fold CV on 330 items)"),
        (f"Main paper uses L{data_llama_layer} "
         f"(AUC={data_llama_auc:.4f}, bootstrap on 160 vulnerable items)"),
        ("Both are real. Supplementary should clarify it shows 5-fold CV values, "
         "not bootstrap. Add a note: 'AUC values are 5-fold CV means; bootstrap "
         "CIs in the main text use a different resampling procedure.'"),
    ))

    # #2: R1-Distill peak AUC and layer
    paper_r1_auc = 0.930
    paper_r1_layer = 31
    data_r1_auc = r1_vuln["auc"]
    data_r1_layer = r1_vuln["layer"]

    if paper_r1_layer != data_r1_layer:
        mismatches.append(Mismatch(
            2, "CRITICAL", "Main paper (multiple locations)",
            find_line("0.930", paper_lines),
            f"R1-Distill peak at L{paper_r1_layer}",
            f"Bootstrap peak at L{data_r1_layer}",
            f"Update peak layer to L{data_r1_layer}",
        ))

    if abs(paper_r1_auc - data_r1_auc) > 0.002:
        mismatches.append(Mismatch(
            2, "CRITICAL", "Main paper (multiple locations)",
            find_line("0.930", paper_lines),
            f"R1-Distill peak AUC = {paper_r1_auc}",
            f"Bootstrap peak AUC = {data_r1_auc:.4f} (rounds to {round3(data_r1_auc)})",
            f"Update AUC to {round3(data_r1_auc)}",
        ))

    # Supplementary says L14=0.929 as peak (CV pipeline)
    cv_r1 = canonical["probes_cv"]["r1_distill_all"]
    mismatches.append(Mismatch(
        2, "MODERATE", "Supplementary Table C.1 vs. main paper",
        None,
        (f"Supplementary bolded L{cv_r1['best_layer']} as peak "
         f"(AUC={cv_r1['best_auc']:.4f}, 5-fold CV)"),
        (f"Main paper uses L{data_r1_layer} "
         f"(AUC={data_r1_auc:.4f}, bootstrap on 160 vulnerable items)"),
        ("Same methodology difference as Llama. "
         "Supplementary should clarify it shows 5-fold CV, not bootstrap."),
    ))

    # #3: Behavioral lure rate mismatches
    # Main paper Table 1 uses DIFFERENT data sources for different numbers:
    #   - Overall rates (27.3%, 2.4%, 21.0%, 7.0%): from 7-cat subset (330 items)
    #   - Per-category rates for original 7 categories: from 7-cat raw files
    #   - Per-category rates for 4 new categories: from 470-item data
    #   - OLMo per-category rates: from 470-item data (only dataset for OLMo)
    # We must compare against the CORRECT source for each number.

    ORIGINAL_7_CATS = {"crt", "base_rate", "conjunction", "syllogism",
                       "arithmetic", "framing", "anchoring"}
    NEW_4_CATS = {"loss_aversion", "sunk_cost", "certainty_effect", "availability"}

    # Load 7-category raw behavioral data for ground truth comparison
    beh_7cat_raw: dict[str, dict[str, int]] = {}
    for mk, path in [
        ("llama", RESULTS / "behavioral" / "llama31_8b_ALL.json"),
        ("r1_distill", RESULTS / "behavioral" / "r1_distill_llama_ALL.json"),
    ]:
        raw = load_json(path)
        cats_data: dict[str, dict[str, int]] = defaultdict(lambda: {"conflict": 0, "lured": 0})
        for item in raw["results"]:
            if item.get("conflict", False):
                cat = item["category"]
                cats_data[cat]["conflict"] += 1
                if item.get("verdict") == "lure":
                    cats_data[cat]["lured"] += 1
        beh_7cat_raw[mk] = {
            cat: round(v["lured"] / v["conflict"] * 100) if v["conflict"] else 0
            for cat, v in cats_data.items()
        }

    # Qwen 7-cat rates from behavioral_complete
    beh_complete = load_json(BEHAVIORAL_7CAT)
    for mk in ["qwen_nothink", "qwen_think"]:
        if mk in beh_complete["models"]:
            beh_7cat_raw[mk] = {}
            for cat, info in beh_complete["models"][mk]["categories"].items():
                if info is not None:
                    beh_7cat_raw[mk][cat] = round(info["lure_rate"] * 100)

    # Paper Table 1 values (as they appear in the tex)
    paper_table1 = {
        "llama": {
            "base_rate": 84, "conjunction": 55, "syllogism": 52,
            "loss_aversion": 0, "sunk_cost": 0, "certainty_effect": 0,
            "availability": 13, "crt": 0, "arithmetic": 0, "framing": 0,
            "anchoring": 0,
        },
        "r1_distill": {
            "base_rate": 4, "conjunction": 0, "syllogism": 0,
            "loss_aversion": 0, "sunk_cost": 0, "certainty_effect": 0,
            "availability": 0, "crt": 3, "arithmetic": 0, "framing": 0,
            "anchoring": 10,
        },
        "qwen_nothink": {
            "base_rate": 56, "conjunction": 95, "syllogism": 0,
            "loss_aversion": 0, "sunk_cost": 0, "certainty_effect": 0,
            "availability": 0, "crt": 0, "arithmetic": 0, "framing": 0,
            "anchoring": 10,
        },
        "qwen_think": {
            "base_rate": 4, "conjunction": 55, "syllogism": 0,
            "loss_aversion": 0, "sunk_cost": 0, "certainty_effect": 0,
            "availability": 0, "crt": 0, "arithmetic": 0, "framing": 0,
            "anchoring": 0,
        },
        "olmo_instruct": {
            "base_rate": 46, "conjunction": 50, "syllogism": 0,
            "loss_aversion": 33, "crt": 3, "arithmetic": 0, "framing": 0,
            "anchoring": 5,
        },
        "olmo_think": {
            "base_rate": 0, "conjunction": 0, "syllogism": 4,
            "loss_aversion": 0, "crt": 3, "arithmetic": 0, "framing": 0,
            "anchoring": 0,
        },
    }

    # Check each model's per-category rates against the CORRECT data source
    for mk in paper_table1:
        for cat, paper_val in paper_table1[mk].items():
            # Determine correct data source
            if mk in ("olmo_instruct", "olmo_think"):
                # OLMo always uses 470-item data
                data_cats = canonical["behavioral"].get(mk, {}).get("per_category_470", {})
                source = "470-item"
            elif cat in ORIGINAL_7_CATS and mk in beh_7cat_raw:
                # Original 7 categories: use 7-cat raw files
                data_val = beh_7cat_raw[mk].get(cat)
                if data_val is not None and paper_val != data_val:
                    mismatches.append(Mismatch(
                        3, "CRITICAL", f"Table 1: {mk}/{cat}",
                        find_line("tab:behavioral", paper_lines),
                        f"{mk} {cat} lure rate = {paper_val}%",
                        f"7-category data shows {data_val}%",
                        f"Update Table 1 {mk} {cat} from {paper_val}% to {data_val}%",
                    ))
                continue
            elif cat in NEW_4_CATS:
                # New categories: use 470-item data
                data_cats = canonical["behavioral"].get(mk, {}).get("per_category_470", {})
                source = "470-item"
            else:
                continue

            if cat in data_cats:
                data_val = int(data_cats[cat]["lure_rate_pct"])
                if paper_val != data_val:
                    mismatches.append(Mismatch(
                        3, "CRITICAL", f"Table 1: {mk}/{cat}",
                        find_line("tab:behavioral", paper_lines),
                        f"{mk} {cat} lure rate = {paper_val}%",
                        f"{source} data shows {data_val}%",
                        f"Update Table 1 {mk} {cat} from {paper_val}% to {data_val}%",
                    ))

    # Check headline overall rates from the paper
    # Paper says: Llama 27.3%, R1 2.4%, Qwen no-think 21.0%, Qwen think 7.0%
